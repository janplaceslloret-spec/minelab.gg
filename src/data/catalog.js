// Helper para saber qué catálogo mostrar según el tipo de server
export function getCatalogForServerType(serverType) {
  if (!serverType) return null;
  const t = String(serverType).toLowerCase();
  if (t === 'paper' || t === 'vanilla' || t === 'spigot') return 'plugins';
  if (t === 'fabric' || t === 'forge' || t === 'neoforge') return 'mods';
  return null;
}

// Filtra el catálogo por compatibilidad con la versión MC del server
export function filterByVersion(items, mcVersion) {
  if (!mcVersion) return items;
  const v = String(mcVersion);
  return items.map(item => {
    const compat = (item.versions || []).some(iv => {
      // 1.20.x matches 1.20.1, 1.20.4, etc.
      if (iv.endsWith('.x')) return v.startsWith(iv.replace('.x', ''));
      return v === iv;
    });
    return { ...item, _compatible: compat };
  });
}
